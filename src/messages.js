/**
 * Retourne 3 messages aléatoires sans répétition parmi la liste.
 * Évite de repiocher les derniers messages déjà envoyés (stockés dans usedIndexes).
 * @param {number[]} usedIndexes - indices déjà utilisés récemment
 * @returns {{ messages: string[], newUsedIndexes: number[] }}
 */
export function piocherMessages(usedIndexes = []) {
  const disponibles = MESSAGES_POUL
    .map((_, i) => i)
    .filter((i) => !usedIndexes.includes(i));

  // Si presque tout a été utilisé, on repart de zéro
  const pool = disponibles.length >= 3 ? disponibles : MESSAGES_POUL.map((_, i) => i);

  const choisis = [];
  const poolCopy = [...pool];

  while (choisis.length < 3 && poolCopy.length > 0) {
    const idx = Math.floor(Math.random() * poolCopy.length);
    choisis.push(poolCopy[idx]);
    poolCopy.splice(idx, 1);
  }

  // Garde uniquement les 30 derniers indices utilisés pour éviter les répétitions proches
  const newUsedIndexes = [...usedIndexes, ...choisis].slice(-30);

  return {
    messages: choisis.map((i) => MESSAGES_POUL[i]),
    newUsedIndexes,
  };
}

/**
 * Génère 3 timestamps aléatoires entre 12h00 et 22h00 Paris, triés dans l'ordre.
 * @returns {number[]} - timestamps en ms
 */
export function genererHorairesAleatoires() {
  const maintenant = new Date();

  const horaires = [];
  while (horaires.length < 3) {
    // Heure random entre 12h et 22h Paris
    const heureRandom = 12 + Math.random() * 10; // 12.0 → 22.0
    const heures = Math.floor(heureRandom);
    const minutes = Math.floor((heureRandom - heures) * 60);

    const date = new Date(maintenant.toLocaleDateString('sv-SE', { timeZone: 'Europe/Paris' }) + `T${String(heures).padStart(2, '0')}:${String(minutes).padStart(2, '0')}:00+02:00`);

    // On ne programme que dans le futur
    if (date.getTime() > Date.now() + 60000) {
      horaires.push(date.getTime());
    }
  }

  return horaires;
}
